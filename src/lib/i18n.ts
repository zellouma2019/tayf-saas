/// جميع الترجمات
const TRANSLATIONS: Record<string, TranslationDict> = { ar, fr, en, tr, es };

/// دالة الترجمة
export function t(key: string, lang?: string | null, vars?: Record<string, string | number>): string {
  const l = lang || "ar";
  const dict = TRANSLATIONS[l] || TRANSLATIONS.ar;
  let text = dict[key] || TRANSLATIONS.ar[key] || key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      text = text.replace(`{${k}}`, String(v));
    }
  }
  return text;
}

/// جلب جميع مفاتيح الترجمة
export function getAllTranslationKeys(): string[] {
  return Object.keys(TRANSLATIONS.ar);
}

